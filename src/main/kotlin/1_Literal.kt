//contoh penggunaan Literal
fun main(args: Array<String>) {
    var intLiteral = 5
    //literal tipe dasar int

    var doubleLiteral = .02
    //Literal untuk tipe dasar Double

    var stringLiteral = "Zizi Andrea"
    //Literal untuk tie dasar String

    var charLiteral = '1'
    //Literal untuk tipe dasar Char

    var boolLiteral = true
    //Literal untuk tipe dasar Boolean

    println("intLiteral = " + intLiteral)
    println("doubleLiteral = " + doubleLiteral)
    println("stringLiteral = " + stringLiteral)
    println("charLiteral = " + charLiteral)
    println("boolLiteral = " + boolLiteral)
}